package co.micol.condition;

public class StringSwitch {

	public static void main(String[] args) {
		// TODO String & case

		String key = "";
		
		switch (key) {
		case "A":
			System.out.println("90~ 100점 사이");
			break;
		case "B":
			System.out.println("80~ 89점 사이");
			break;
		case "C":
			System.out.println("70~ 79점 사이");
			break;
		case "D":
			System.out.println("60~ 69점 사이");
			break;
		case "F":
			System.out.println("59점 이하");
			break;
			
		}
		
	}

}
