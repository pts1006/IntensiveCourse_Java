package co.micol.classExam;

public class ForTest {

	public void forPrint() {
		
		for(int i = 0; i <= 7 ; i++) {
			System.out.print("*");
		}
		
		System.out.println();
		
		/*
		  
		  범위 지시자
		  
			  public : 범위 제일 넓음. 어디서든 쓰임.
			  private : 내 안에서만 쓸 수 있음
			  protected : public을 생략할 경우로, 클래스가 생성은 되는데, 안에 있는 걸 아무것도 사용할 수 없음.
			  
			  
			  void : 실행만 하면 장땡 ex) public void
			  int, float, object 따위들은 return이 필요. ex) public int
		  
		  class는 리턴밸류가 없다.
		  
		*/
		
	}
	
}